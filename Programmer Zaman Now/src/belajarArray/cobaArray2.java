package belajarArray;

public class cobaArray2 {

    public static void main(String[] args) {

        char[] ejaHuruf = {'T', 'E', 'U', 'K', 'U'}; //variable array
        String[] namaLengkap = {"Teuku", "Reynaldi", "Putra", "Irwansyah"};

        //untuk char[]
        System.out.println(ejaHuruf[0]);
        System.out.println(ejaHuruf[1]);
        System.out.println(ejaHuruf[2]);
        System.out.println(ejaHuruf[3]);
        System.out.println(ejaHuruf[4]);
        System.out.println();

        //untuk String[]
        System.out.println(namaLengkap[0]);
        System.out.println(namaLengkap[1]);
        System.out.println(namaLengkap[2]);
        System.out.println(namaLengkap[3]);

    }
}