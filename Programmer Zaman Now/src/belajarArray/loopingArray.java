package belajarArray;

public class loopingArray {
    public static void main(String[] args) {

        String[] namaLengkap = {"Teuku", "Reynaldi", "Putra", "Irwansyah"};

        for (int i = 0; i < namaLengkap.length; i++) {

            System.out.println(namaLengkap[i]);
        }
    }
}
