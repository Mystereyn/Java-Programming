package Looping;

public class While {
    public static void main(String[] args) {

        {
            int i = 0;

            while (i <= 10) {
                System.out.println(i);
                i++;
            }
            System.out.println();
        }

        //jika objek String dimasukkan ke WHile looping.
        {
            String nama = "Teuku Reynaldi Putra";
            int j = 0;

            while (j <= 20) {
                System.out.println("Nama lengkap saya = " + nama);
                j++;
            }

        }

    }
}
