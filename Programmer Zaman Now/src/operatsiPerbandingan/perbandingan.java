package operatsiPerbandingan;

public class perbandingan {

    public static void main(String[] args) {

        int nilai1 = 100;
        int nilai2 = 100;

        System.out.println(nilai1 > nilai2);
        System.out.println(nilai1 < nilai2);
        System.out.println(nilai1 >= nilai2);
        System.out.println(nilai1 <= nilai2);
        System.out.println(nilai1 == nilai2);
        System.out.println(nilai1 != nilai2);
        
    }
}
