package unaryOperator;

public class unaryOperator {
    public static void main(String[] args) {

        int d = 100;
        d++; //ini 10 ++ (bertambah satu)
        System.out.println(d);

        d--; //outpunyt adalah 100 -- (berkurang satu)
        System.out.println(d);
    }
}
