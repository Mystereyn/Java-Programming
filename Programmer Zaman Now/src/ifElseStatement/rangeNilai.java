package ifElseStatement;

public class rangeNilai {
    public static void main(String[] args) {
        
        // 0 - 50 = D
        // 51-70  = C
        // 71-80  = B
        // 81-100  = A
        int nilai = 20;
        if (nilai <= 50) {
            System.out.println("D");
        } else if (nilai <= 70 ) {
            System.out.println("C");
        } else if (nilai <= 70 ) {
            System.out.println("B");
        } else if (nilai <= 70 ) {
            System.out.println("A");
        }

    }
    
}
