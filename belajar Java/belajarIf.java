public class belajarIf { //Sesuai nama file pada domain file.
    public static void main(String[] args) { //syntax wajib pada java, sama halnya seperti clearscreen pada pascal 
        int berat = 92; // ini adalah variabel yang akan di eksekusi oleh java
        if (berat>90) { // ini adalah pernyataan if 
            System.out.println("Berat lebih besar daripada 90");
        }
    }
}
